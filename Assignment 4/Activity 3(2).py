
#A program calculate and display the distance from miles into kilometers, meters, and centimeters.
miles = int(input("Input miles: "))
kilometers = miles * 1.609344
meters = miles * 1.609
centimeters = miles * 160.934
print("The distance in kilometers is " +str(kilometers) +" in")
print("The distance in meters is " +str(meters) +" yd")
print("The distance in centimeters is " +str(centimeters) +" ft")
