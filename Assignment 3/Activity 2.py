#Asking user age
age=float(input("Enter age:"))
print("Person is ",age,"years old.")
#calculate print age in Months
print("Person is ",age*12,"months old")
#calculate print age in days
print("Person is ",age*12*365+age/4,"days old")
#calculate print age in hours
print("Person is ",(age*12*365+age/4)*24,"hours old")
#calculate print age in seconds
print("Person is ",(age*12*365+age/4)*24*60*60,"seconds old")
